<script>
   // JavaScript to handle quantity change
   document.querySelectorAll('.quantity-btn').forEach(button => {
    button.addEventListener('click', function (event) {
        // Prevent any default behavior (like form submission or page reload)
        event.preventDefault();
        
        const quantityDisplay = this.parentElement.querySelector('.item-quantity-display');
        const priceDisplay = this.parentElement.parentElement.querySelector('.item-price');
        const totalDisplay = this.parentElement.parentElement.querySelector('.item-total');
        
        let currentValue = parseInt(quantityDisplay.textContent);
        const unitPrice = parseFloat(priceDisplay.textContent.replace(/[^0-9.-]+/g, "")); // Lấy giá sản phẩm, loại bỏ dấu phân cách

        // Increase or decrease quantity based on button clicked
        if (this.classList.contains('increase')) {
            currentValue += 1; // Increase the value
        } else if (this.classList.contains('decrease')) {
            currentValue -= 1; // Decrease the value
        }

        // Ensure the value doesn't go below the minimum (1)
        if (currentValue >= 1) {
            quantityDisplay.textContent = currentValue;
        }

        // Tính toán lại tổng giá
        const totalPrice = (unitPrice * currentValue).toFixed(3); // Sử dụng .toFixed(2) để giữ 2 chữ số thập phân

        totalDisplay.textContent = `${totalPrice} VNĐ`;
        
        // Cập nhật lại tổng giá tiền ở phần dưới giỏ hàng
        const allTotalPrices = document.querySelectorAll('.item-total');
        let newTotal = 0;

        allTotalPrices.forEach(item => {
            newTotal += parseFloat(item.textContent.replace(/[^0-9.-]+/g, ""));
        });

        document.getElementById('total-price').textContent = newTotal.toFixed(3); // Cập nhật tổng giỏ hàng với 2 chữ số thập phân
    });
});


</script>